function stripReviewModal(html) {
  let changed = false;
  // Удалим предшествующий маркер-комментарий + сам блок review-modal по балансу div.
  const openTag = '<div class="modal-overlay" id="review-modal">';
  const marker = '<!-- ===== МОДАЛЬНОЕ ОКНО СБОРА ОТЗЫВОВ ===== -->';
  let idx = html.indexOf(openTag);
  while (idx !== -1) {
    // найдём закрывающий </div> балансом
    const re = /<div[\s>]|<\/div>/g;
    re.lastIndex = idx;
    let depth = 0, closeIdx = -1, m;
    while ((m = re.exec(html)) !== null) {
      if (m[0] === "</div>") {
        depth--;
        if (depth === 0) { closeIdx = m.index + m[0].length; break; }
      } else depth++;
    }
    if (closeIdx === -1) break;
    let start = idx;
    // захватим маркер-комментарий перед блоком
    const before = html.slice(0, start);
    const markerPos = before.lastIndexOf(marker);
    if (markerPos !== -1 && before.slice(markerPos + marker.length).trim() === "") {
      start = markerPos;
    }
    html = html.slice(0, start).replace(/[ \t]*\n[ \t]*$/, "") + "\n" + html.slice(closeIdx);
    changed = true;
    idx = html.indexOf(openTag);
  }
  return { html, changed };
}

