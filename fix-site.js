function faqHtml(items) {
  const cards = items.map((it) =>
`            <div class="faq-item">
              <button type="button" class="faq-trigger" aria-expanded="false">
                <span>${it.q}</span>
                <span class="faq-arrow">▼</span>
              </button>
              <div class="faq-panel">
                <div class="faq-content">
                  <p>
                    ${it.a}
                  </p>
                </div>
              </div>
            </div>`
  ).join("\n\n");
  return `<div class="faq-accordion-box">\n${cards}\n          </div>`;
}

