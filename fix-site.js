function reconstructCityService(C) {
  const headEnd = C.indexOf("</head>");
  if (headEnd === -1) throw new Error("no </head>");
  let head = C.slice(0, headEnd + "</head>".length);

  const bodyOpen = C.indexOf("<body");
  const bodyOpenEnd = C.indexOf(">", bodyOpen) + 1;
  const bodyTag = C.slice(bodyOpen, bodyOpenEnd);

  const navStart = C.indexOf('<a class="skip-link"', bodyOpenEnd);
  const mainIdx = C.indexOf("<main", navStart);
  const nav = C.slice(navStart, mainIdx); // skip-link + header + mobile-nav

  const sectionStart = C.indexOf("<section", mainIdx);
  const mainClose = C.indexOf("</main>", sectionStart);
  if (sectionStart === -1 || mainClose === -1) throw new Error("cannot locate real content");
  let realContent = C.slice(sectionStart, mainClose).replace(/\s+$/, "");

  const hasGlow = /class="glow-bg"/.test(C.slice(mainIdx, sectionStart));
  const glow = `    <!-- Background Glow FX -->\n    <div class="glow-bg">\n      <div class="glow-orb orb-1"></div>\n      <div class="glow-orb orb-2"></div>\n    </div>\n`;

  // Хвост: от ПОСЛЕДНЕГО footer до конца
  const lastFooter = C.lastIndexOf("<footer class=\"site-footer\">");
  const tail = C.slice(lastFooter);
  const footerEnd = tail.indexOf("</footer>") + "</footer>".length;
  const footer = tail.slice(0, footerEnd);

  const seg = (a, b) => {
    const ia = C.lastIndexOf(a), ib = C.lastIndexOf(b);
    if (ia === -1 || ib === -1 || ib <= ia) return "";
    return C.slice(ia, ib).replace(/\s+$/, "");
  };
  const orderModal = seg(MARK.order, MARK.review);
  const callBtn = seg(MARK.call, MARK.top);
  const scrollTop = seg(MARK.top, MARK.scripts);
  // scripts: от маркера до </body>
  const sIdx = C.lastIndexOf(MARK.scripts);
  const bodyClose = C.lastIndexOf("</body>");
  const scripts = sIdx !== -1 && bodyClose !== -1 ? C.slice(sIdx, bodyClose).replace(/\s+$/, "") : "";

  const parts = [
    head,
    "",
    bodyTag,
    nav.replace(/\s+$/, ""),
    `<main id="main">`,
    hasGlow ? glow : "",
    realContent,
    `</main>`,
    footer,
    orderModal,
    callBtn,
    scrollTop,
    scripts,
    "",
    "</body>",
    "</html>",
    "",
  ].filter((p) => p !== "" || true);
  return parts.join("\n");
}

