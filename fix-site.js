function run() {
  if (ONLY) {
    ONLY.forEach((rel) => {
      const fp = path.join(ROOT, rel);
      if (!fs.existsSync(fp)) { console.log("skip (нет файла):", rel); return; }
      const isCity = rel.includes("/");
      if (isCity) {
        const [city, svc] = rel.split("/");
        if (svc === "index.html") processCityIndex(city);
        else processCityService(city, svc);
      } else if (rel === "index.html") processRootIndex();
      else processRootService(rel);
    });
  } else {
    CITY_LIST.forEach((c) => {
      SERVICE_FILES.forEach((s) => processCityService(c, s));
      processCityIndex(c);
    });
    ROOT_SERVICE_FILES.forEach((s) => processRootService(s));
    processRootIndex();
    ["about.html", "gruzoperevozki.html", "thank-you.html"].forEach((s) => processRootMisc(s));
  }
  console.log("\n=== fix-site.js отчёт ===");
  console.log("DRY:", DRY);
  console.log(JSON.stringify(stats, null, 2));
}

run();
