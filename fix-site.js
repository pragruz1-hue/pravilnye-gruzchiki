function faqJson(items) {
  const obj = {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    mainEntity: items.map((it) => ({
      "@type": "Question",
      name: it.q,
      acceptedAnswer: { "@type": "Answer", text: it.a },
    })),
  };
  return `<script type="application/ld+json">\n      ${JSON.stringify(obj, null, 2).replace(/\n/g, "\n      ")}\n    </script>`;
}

// Заменить содержимое HTML-аккордеона FAQ на новое.
// Поддерживает 2 разметки: .faq-accordion-box (faq-trigger) и .u-style-075 (details).
function replaceFaqAccordion(html, items) {
  // 1) faq-accordion-box
  let open = html.indexOf('<div class="faq-accordion-box">');
  if (open !== -1) {
    let depth = 0, closeIdx = -1;
    const re = /<div[\s>]|<\/div>/g;
    re.lastIndex = open;
    let m;
    while ((m = re.exec(html)) !== null) {
      if (m[0] === "</div>") {
        depth--;
        if (depth === 0) { closeIdx = m.index; break; }
      } else depth++;
    }
    if (closeIdx !== -1) {
      return { html: html.slice(0, open) + faqHtml(items) + html.slice(closeIdx + "</div>".length), ok: true };
    }
  }
  // 2) u-style-075 (details-style)
  open = html.indexOf('<div class="u-style-075">');
  if (open !== -1) {
    let depth = 0, closeIdx = -1;
    const re = /<div[\s>]|<\/div>/g;
    re.lastIndex = open;
    let m;
    while ((m = re.exec(html)) !== null) {
      if (m[0] === "</div>") {
        depth--;
        if (depth === 0) { closeIdx = m.index; break; }
      } else depth++;
    }
    if (closeIdx !== -1) {
      return { html: html.slice(0, open) + faqDetailsHtml(items) + html.slice(closeIdx + "</div>".length), ok: true };
    }
  }
  return { html, ok: false };
}

// Заменить/вставить FAQPage JSON-LD в <head>
function replaceFaqJsonLd(head, items) {
  const block = faqJson(items);
  // Важно: (?!</script>) не даёт пересечь границу блока, чтобы не «съесть»
  // соседний LocalBusiness/Service блок, идущий перед FAQPage.
  const re = /<script type="application\/ld\+json">\s*\{(?:(?!<\/script>)[\s\S])*?"@type"\s*:\s*"FAQPage"(?:(?!<\/script>)[\s\S])*?\}\s*<\/script>/;
  if (re.test(head)) {
    return { head: head.replace(re, block), ok: true };
  }
  // вставить перед </head>
  return { head: head.replace(/<\/head>/, block + "\n  </head>"), ok: "inserted" };
}

